package vinodha.java;

public class ArrayLength {

	public static void main(String[] args) {
		int[] number=new int[6];
		String[] str= {"apple","ball","cat","dog"};
		int a=number.length;
		int b=str.length;
		System.out.println("the length of number is:"+a);
		System.out.println("the length of str is:"+b);

	}

}